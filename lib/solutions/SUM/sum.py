#!/usr/bin/env python

def sum(intA, intB):
    """Add two integers, both with value between 0-100

    :param intA: First integer to add
    :type intA: int
    :param intB: Second integer to add
    :type intB: int
    :return: The sum of the parameters
    :rtype: int
    """
    return intA + intB





